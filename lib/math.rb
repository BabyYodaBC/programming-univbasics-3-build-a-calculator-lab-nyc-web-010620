def addition(num1, num2)
  sum = num2 + num1
  return sum
end

def subtraction(num1, num2)
  diff = num2 - num1
  return diff
end

def division(num1, num2)
  quo = num2 / num1
  return quo
end

def multiplication(num1, num2)
  prod = num2 * num1
  return prod
end

def modulo(num1, num2)
  rem = num2 % num1
  return rem
end

def square_root(num)
  squa = Math.sqrt(num)
  return squa
end